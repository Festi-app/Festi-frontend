export type BoothCategory = '정보' | '체험' | '마켓' | '활동'
export type PermDay = 1 | 2 | 3
export type PermTime = '주간' | '야간'

export interface DefaultPermission {
  id: string
  zoneId: string
  sections: number[]
  orgId: string
  orgName: string
  color: string
  category: BoothCategory
  day: PermDay
  time: PermTime
}

const CATEGORY_COLOR: Record<BoothCategory, string> = {
  정보: '#00C6E0',
  체험: '#9B72E8',
  마켓: '#F5A623',
  활동: '#00D49C',
}

function toCategory(cat?: string): BoothCategory {
  if (cat === '마켓') return '마켓'
  if (cat === '체험') return '체험'
  if (cat === '정보') return '정보'
  return '활동'
}

export const DEFAULT_PERMISSIONS: DefaultPermission[] = [
  ...[...DAY_BOOTHS, ...NIGHT_BOOTHS]
    .filter((b) => b.zoneId && b.sections?.length)
    .flatMap((b) =>
      ([1, 2, 3] as PermDay[]).map((day) => ({
        id: `default-${b.id}-d${day}-${b.zoneId}`,
        zoneId: b.zoneId!,
        sections: b.sections!,
        orgId: `default-${b.id}`,
        orgName: b.name,
        color: CATEGORY_COLOR[toCategory(b.cat)],
        category: toCategory(b.cat),
        day,
        time: (DAY_BOOTHS.includes(b) ? '주간' : '야간') as PermTime,
      }))
    ),
]

